% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
var = get(handles.edit1,'string');
var1 = get(handles.checkbox1,'Value');
var2 = get(handles.edit9,'string');
var2 =str2double(var2);
var3 = get(handles.edit20,'string');
var3 =str2double(var3);

a = main(var,var1,var2);
b = d_sort(var);
c = choose_path(var,var1,var2);
d = probability_success(var,var1,var2);
e = critical_path(var,var1,var2);
% cp = max(wghts(var,var2));

 e = e(:,1);
 e = fnd(e,1);
% len =length(e);
% for n = 1: len
% es(n,:)  = (max(e) - e(n));
% reserve(n,:) = [n,es(n)];
% end
len =length(e);

% if (var1 == 1)
%     var2 = len;
% end


disp_select = sum(e.^2)/var2;
n =var2/(var2-1);
disp_not_select =n*disp_select;
CKO_not_select = sqrt(n * disp_select);
CKO_select = sqrt(disp_select);

switch var3
    case 0.90
        var3 = 0.95;
    case 0.95
        var3 = 0.975;
    case 0.99
        var3 = 0.995;
    otherwise 
        var3 = 0.975;
end
       
% t-критерий Стьюдента
% 0.95 = 0.975
% 0.90 = 0.95
% 0.99 = 0.995
t_alpha = tinv(var3,len-1);
interval_l = round(a - t_alpha*(disp_not_select /sqrt(len)));
interval_r = round(a + t_alpha*(disp_not_select /sqrt(len)));

x= [interval_l,interval_l,interval_r,interval_r];
y=[0,1,1,0];
area(handles.axes2,x,y,'FaceColor','g','LineStyle',':','LineWidth',3);

set(handles.edit15,'String',num2str(a));
set(handles.edit16,'String',num2str(disp_select));
set(handles.edit17,'String',num2str(disp_not_select));
set(handles.edit18,'String',num2str(CKO_not_select));
set(handles.edit19,'String',num2str(CKO_select));

% set(handles.edit5,'String',num2str(a));
set(handles.edit3,'String',num2str(b));
set(handles.edit4,'String',num2str(c));
set(handles.edit6,'String',num2str(d));
% set(handles.edit8,'String',num2str(round(transpose(reserve))));

if(d >= 0.8)
    set(handles.edit7,'String','высокая вероятность выполнить все работы в срок');
elseif (d>= 0.6)
    set(handles.edit7,'String','средняя вероятность выполнить все работы в срок');
elseif (d > 0.3)
    set(handles.edit7,'String','низкая вероятность выполнить все работы в срок');
else
    set(handles.edit7,'String','Вероятность выполнения работы в срок крайне мала. Рекомендуется повторить расчёт');
end





